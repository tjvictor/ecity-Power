package ecity_power.model.weChat;

public class Activity_Participate {

    private String id;
    private String activityRegisterId;
    private String participateId;
    private String participateName;
    private String weight;
    private String date;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getActivityRegisterId() {
        return activityRegisterId;
    }

    public void setActivityRegisterId(String activityRegisterId) {
        this.activityRegisterId = activityRegisterId;
    }

    public String getParticipateId() {
        return participateId;
    }

    public void setParticipateId(String participateId) {
        this.participateId = participateId;
    }

    public String getParticipateName() {
        return participateName;
    }

    public void setParticipateName(String participateName) {
        this.participateName = participateName;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
